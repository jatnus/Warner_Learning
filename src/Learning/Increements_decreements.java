package Learning;

public class Increements_decreements {

    public static void main(String[] args){
        int x =1;
      int  y = x++;
       System.out.println(x);
       System.out.println(y);

       int a=2;
       int b = ++a;
       System.out.println(a);
       System.out.println(++b);

       System.out.println(a);
       System.out.println(b);

    }

}
